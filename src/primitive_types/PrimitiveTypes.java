package primitive_types;

public class PrimitiveTypes {
    public static void main(String[] args) {
        String nome = "Michael";
        int idade = 19;
        double peso = 60.55;
        System.out.println("Nome = " + nome);
        System.out.println("Idade = " + idade);
        System.out.println("Peso = " + peso);
    }
}
